   <h1 class="tituloClases">Lista de estrellas  por orden Alfabético</h1><br>
   <p class="tituloClases">En sentido general, una <b>estrella</b> es todo objeto astronómico que brilla con luz propia;
    mientras que en términos más técnicos y precisos podría <br> decirse que <i>se trata de una 
    esfera de plasma que mantiene su forma gracias a un equilibrio hidrostático de fuerzas.</i>
     El equilibrio se produce <br> esencialmente entre la fuerza de gravedad, que empuja la materia
    hacia el centro de la estrella, y la presión que ejerce el plasma hacia fuera, que, tal <br>
    como sucede en un gas, tiende a expandirlo. La presión hacia fuera depende de la temperatura,
    que en un caso típico como el del Sol se mantiene con <br> la energía producida en el interior de
    la estrella.Este equilibrio seguirá esencialmente igual en la medida de que la 5 estrella 
    mantenga el mismo ritmo <br> de producción energética.Sin embargo, como se explica más adelante, 
    este ritmo cambia a lo largo del tiempo, generando variaciones en las <br> propiedades físicas 
    globales del astro que constituyen parte de su evolución.</p>

